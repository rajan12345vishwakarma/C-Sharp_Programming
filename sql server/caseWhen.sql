--CASE STATEMENT
UPDATE students
SET gender = CASE 
WHEN gender = 'M' THEN 'F'
WHEN gender = 'F' THEN 'M'
ELSE NULL
END 

--CASE EXPRESSION
select cust_name, case OPENING_AMT
WHEN  5000 THEN 'PRIME CUSTOMER'
WHEN  7000 THEN 'PLUS CUSTOMER'
ELSE 'REGULAR CUSTOMER'
END AS CUSTOMER_STATUS
FROM CUSTOMER

--CASE STATEMENT
SELECT CUST_NAME, OPENING_AMT,
CASE WHEN OPENING_AMT > 7000 THEN 'PRIME CUSTOMER'
WHEN OPENING_AMT > 500 THEN 'PLUS CUSTOMER'
ELSE 'REGULAR CUSTOMER'
END AS CUSTOMER_STATUS
FROM CUSTOMER
